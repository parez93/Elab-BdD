public class Query6Bean {
    private String nomeCL;
    private String durata;
    private String daS;
    private String drS;   
    public Query6Bean() {
        this.nomeCL = null;
        this.durata = null;
        this.daS = null;
        this.drS = null;
    }

    public String getNomeCL() {
        return nomeCL;
    }

    public void setNomeCL(String nomeCL) {
        this.nomeCL = nomeCL;
    }

    public String getDurata() {
        return durata;
    }

    public void setDurata(String durata) {
        this.durata = durata;
    }

    public String getDaS() {
        return daS;
    }

    public void setDaS(String daS) {
        this.daS = daS;
    }

    public String getDrS() {
        return drS;
    }

    public void setDrS(String drS) {
        this.drS = drS;
    }
    
    
     
}
